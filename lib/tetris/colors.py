class Colors:
    dark_grey = 0x1a1f28
    green = 0x2fe617
    red = 0xe81212
    orange = 0xe27411
    yellow = 0xedea04
    purple = 0xa600f7
    cyan = 0x15ccd1
    blue = 0x0d40d8

    @classmethod
    def get_colors(cls):
        return [cls.dark_grey, cls.green, cls.red, cls.orange, cls.yellow, cls.purple, cls.cyan, cls.blue]
